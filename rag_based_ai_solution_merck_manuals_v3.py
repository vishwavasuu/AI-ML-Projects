def _build_context_from_hits(hits, max_chars_per_chunk=1800):
    """Build a numbered context block + a printable sources list from your retrieve() hits."""
    ctx_lines, sources = [], []
    for i, h in enumerate(hits, start=1):
        chunk_text = Path(h["chunk_path"]).read_text(encoding="utf-8", errors="ignore")
        if len(chunk_text) > max_chars_per_chunk:
            chunk_text = chunk_text[:max_chars_per_chunk]
        src_label = f"{Path(h['chunk_path']).name} · {h.get('section','unknown')} · {h.get('citation','')}"
        ctx_lines.append(f"[{i}] Source: {src_label}\n{chunk_text.strip()}")
        sources.append({"n": i, "path": h["chunk_path"], "section": h.get("section","unknown"), "citation": h.get("citation","")})
    return "\n\n".join(ctx_lines), sources

