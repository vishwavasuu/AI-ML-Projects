#The following function retrieves using FAISS search
def retrieve(query: str, k: int = TOP_K):
    q = emb_model.encode([query], normalize_embeddings=True).astype("float32")
    D, I = index.search(q, k)
    I = I[0].tolist()
    D = D[0].tolist()
    results = []
    for rank, (row, score) in enumerate(zip(I, D)):
        m = meta[row]
        text = Path(m["chunk_path"]).read_text(encoding="utf-8", errors="ignore")[:2000]  # cap display length
        results.append({
            "rank": rank+1,
            "score": float(score),
            "citation": m["citation"],
            "section": m["section_title"],
            "chunk_path": m["chunk_path"],
            "preview": text[:600].strip().replace("\n", " ")  # short preview
        })
    return results

