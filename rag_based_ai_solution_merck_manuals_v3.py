from pathlib import Path

def qa_rag_llama(question: str, k: int = 6, score_floor: float = 0.25, max_new_tokens=480):
    hits = retrieve(question, k=k)
    hits = [h for h in hits if h.get("score", 0.0) >= score_floor]
    if not hits:
        return "I don’t have enough information in the retrieved context to answer this.", []

    #Vishwa: This is where we build the context from the Merck Manual
    try:
        context_block, sources = _build_context_from_hits(hits, max_chars_per_chunk=1800)
    except NameError:
        from pathlib import Path
        ctx_lines, sources = [], []
        for i, h in enumerate(hits, start=1):
            chunk_text = Path(h["chunk_path"]).read_text(encoding="utf-8", errors="ignore")
            if len(chunk_text) > 1800: chunk_text = chunk_text[:1800]
            src_label = f"{Path(h['chunk_path']).name} · {h.get('section','unknown')} · {h.get('citation','')}"
            ctx_lines.append(f"[{i}] Source: {src_label}\n{chunk_text.strip()}")
            sources.append({"n": i, "path": h["chunk_path"], "section": h.get("section","unknown"), "citation": h.get("citation","")})
        context_block = "\n\n".join(ctx_lines)

    sys_msg = ("You are a cautious clinical assistant. Answer ONLY using the CONTEXT. "
               "Use inline citations like [n] that refer to the numbered sources. "
               "If the answer is not fully supported by CONTEXT, say you don’t have enough information.")
    user_msg = f"CONTEXT:\n{context_block}\n\nQUESTION:\n{question}\n\nANSWER (with inline [n] citations):"
    messages = [
        {"role": "system", "content": sys_msg},
        {"role": "user",   "content": user_msg},
    ]
    answer = _generate_chat(messages, max_tokens=max_new_tokens, temperature=0.0)
    return answer, sources

