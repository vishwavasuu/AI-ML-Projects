"""## Output Evaluation

Let us now use the LLM-as-a-judge method to check the quality of the RAG system on two parameters - retrieval and generation. We illustrate this evaluation based on the answeres generated to the question from the previous section.

- We are using the same Mistral model for evaluation, so basically here the llm is rating itself on how well he has performed in the task.
"""

qna_system_message = (
    "You are a cautious clinical assistant. Answer ONLY from the provided CONTEXT. "
    "Use inline [n] citations where possible. If information is missing, say so."
)
qna_user_message_template = "CONTEXT:\n{context}\n\nQUESTION:\n{question}\n\nANSWER:"

groundedness_rater_system_message  = """\
You are a strict scientific evaluator. Rate how well the ANSWER is grounded in the provided CONTEXT.
Rules:
- Consider only information present in CONTEXT as valid support.
- Penalize any claims not supported by CONTEXT.
Return ONLY a JSON object: {"score": <0-5>, "justification": "<1-2 sentences>"}.
Scoring:
0: not grounded; 1: mostly unsupported; 2: partial; 3: mostly grounded; 4: well grounded; 5: fully grounded.
"""

relevance_rater_system_message = """\
You are an IR evaluator. Rate how relevant the CONTEXT is for answering the QUESTION.
- Judge topical match and whether key facts are present.
Return ONLY a JSON object: {"score": <0-5>, "justification": "<1-2 sentences>"}.
Scoring:
0=irrelevant; 1=barely; 2=partial; 3=moderate; 4=high; 5=perfect.
"""

user_message_template = """\
CONTEXT:
{context}

QUESTION:
{question}

ANSWER:
{answer}

Provide your evaluation per instructions."""

from pathlib import Path

def generate_ground_relevance_response(user_input, k=3, max_tokens=200, temperature=0.0):
    """
    Returns:
      groundedness_text_json, relevance_text_json, answer_text
    - Uses FAISS `retrieve(...)` to build CONTEXT
    - Uses llama_cpp via `_generate_chat(...)`
    """
    global qna_system_message, qna_user_message_template
    global groundedness_rater_system_message, relevance_rater_system_message, user_message_template

    # --- Retrieve relevant chunks from FAISS ---
    hits = retrieve(user_input, k=k)  # each hit: {chunk_path, section, citation, score, ...}

    # --- Build a bounded CONTEXT from file contents ---
    MAX_CHARS_PER_CHUNK = 1500
    MAX_CONTEXT_CHARS   = 6000
    context_parts, total = [], 0

    for h in hits:
        try:
            t = Path(h["chunk_path"]).read_text(encoding="utf-8", errors="ignore").strip()
        except Exception:
            t = ""
        if not t:
            continue
        t = t[:MAX_CHARS_PER_CHUNK]
        if total + len(t) > MAX_CONTEXT_CHARS:
            break
        context_parts.append(t)
        total += len(t)

    context_for_query = "\n\n".join(context_parts) if context_parts else "(no relevant context retrieved)"

    # --- Produce the answer to be judged (RAG-style) ---
    qna_messages = [
        {"role": "system", "content": qna_system_message},
        {"role": "user",   "content": qna_user_message_template.format(
            context=context_for_query, question=user_input
        )},
    ]
    # llama_cpp chat (deterministic for evaluation)
    answer_text = _generate_chat(qna_messages, max_tokens=max_tokens, temperature=temperature)

    # --- Judges: GROUNDEDNESS ---
    grounded_msgs = [
        {"role": "system", "content": groundedness_rater_system_message},
        {"role": "user",   "content": user_message_template.format(
            context=context_for_query, question=user_input, answer=answer_text
        )},
    ]
    groundedness_text = _generate_chat(grounded_msgs, max_tokens=160, temperature=0.0)

    # --- Judges: RELEVANCE ---
    relevance_msgs = [
        {"role": "system", "content": relevance_rater_system_message},
        {"role": "user",   "content": user_message_template.format(
            context=context_for_query, question=user_input, answer=answer_text
        )},
    ]
    relevance_text = _generate_chat(relevance_msgs, max_tokens=160, temperature=0.0)

    return groundedness_text, relevance_text, answer_text

